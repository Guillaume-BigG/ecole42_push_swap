#include "libft.h"

char	*ft_strdup(const char *src)
{
	size_t	i;
	char	*dup;

	i = ft_strlen(src);
	dup = (char *)malloc(sizeof(char) * (i + 1));
	if (!dup)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
